from sqlalchemy import Column, Integer, String

from src.config.database import Base


class HouseholdEvaluation(Base):
    __tablename__ = "fiche_eval_menage"
    
    id = Column("ID_EVA", Integer, primary_key=True, index=True)
    structure = Column("STRUCT", Integer, index=True)
    platform = Column("PLATFORM", Integer, index=True)
    cs_code = Column("CS_CODE", String, index=True)
    household_code = Column("CODEMEN", String, index=True)
    evaluation1_date = Column("DATEEV_1", String, index=True)
    evaluation2_date = Column("DATEEV_2", String, index=True)
    agent = Column("AGENT", String, index=True)
    region = Column("IDREG", Integer, index=True)
    department = Column("IDDEPART", Integer, index=True)
    sub_prefecture = Column("IDSP", Integer, index=True)
    commune = Column("IDCOM", Integer, index=True)
    district = Column("IDQUAR", Integer, index=True)
    nutritional_security1 = Column("SEC_1", String)
    water_accessibility1 = Column("MENEAU_1", String)
    household_health1 = Column("SOINSTE_1", String)
    eduction1 = Column("EDUC_1", String)
    household_income_1 = Column("REVENU_1", String)
    household_employment_1 = Column("MENEMP_1", String)
    shelter_housing_1 = Column("ABRI_1", String)
    head_household_1 = Column("RESPMEN_1", String)
    protection_1 = Column("PROT_1", String)
    vulnerability_score__1 = Column("score_ve_1", String)
    other_score_1 = Column("score_aut_1", String)
    total_score_1 = Column("tot_scor_1", Integer)
    category_vulnerability_1 = Column("cat_ve_1", Integer)
    general_category_1 = Column("cat_gle_1", Integer)
    nutritional_security_2 = Column("SEC_2", String)
    water_accessibility_2 = Column("MENEAU_2", Integer)
    household_health_2 = Column("SOINSTE_2", Integer)
    eduction_2 = Column("EDUC_2", String)
    income_household_2 = Column("REVENU_2", String)
    household_employment_2 = Column("MENEMP_2", String)
    shelter_housing_2 = Column("ABRI_2", String)
    head_household_2 = Column("RESPMEN_2", String)
    protection_2 = Column("PROT_2", String)
    vulnerability_score_2 = Column("score_ve_2", String)
    other_score_2 = Column("score_aut_2", String)
    total_score_2 = Column("tot_scor_2", String)
    vulnerability_category_2 = Column("cat_ve_2", String)
    general_category_2 = Column("cat_gle_2", String)
    registration_status = Column("Etat", Integer)
    registration_status_change = Column("Etat_modif", Integer)
    registration_status_report = Column("Etat_stat", Integer)
    registration_status_remove = Column("Etat_del", Integer)
    created_by = Column("idadd", String)
    created_at = Column("dateheure", String)
    changed_by = Column("idmodif", String)
    changed_at = Column("dateheure_modif", String)

